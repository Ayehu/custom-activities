    public class ZM_Add_a_Device : IActivityAsync
    {


    
    public string Jsonkeypath = "devices";
    
    public string apikey = "";
    
    public string password1 = "";
    
    public string mac_address = "";
    
    public string type_p = "";
    
    public string model = "";
    
    public string assigned_to = "";
    
    public string display_name = "";
    
    private bool omitJsonEmptyorNull = true;
    
    private string contentType = "application/json";
    
    private string endPoint = "https://api.zoom.us";
    
    private string httpMethod = "POST";
    
    private string _uriBuilderPath;
    
    private string _postData;
    
    private System.Collections.Generic.Dictionary<string, string> _headers;
    
    private System.Collections.Generic.Dictionary<string, string> _queryStringArray;
    
    private string uriBuilderPath {
        get {
            if (string.IsNullOrEmpty(_uriBuilderPath)) {
_uriBuilderPath = "v2/phone/devices";
            }
return _uriBuilderPath;
        }
        set {
            this._uriBuilderPath = value;
        }
    }
    
    private string postData {
        get {
            if (string.IsNullOrEmpty(_postData)) {
_postData = string.Format("{{ \"mac_address\": \"{0}\",  \"type\": \"{1}\",  \"model\": \"{2}\",  \"assigned_to\": \"{3}\",  \"display_name\": \"{4}\" }}",mac_address,type_p,model,assigned_to,display_name);
            }
return _postData;
        }
        set {
            this._postData = value;
        }
    }
    
    private System.Collections.Generic.Dictionary<string, string> headers {
        get {
            if (_headers == null) {
_headers = new Dictionary<string, string>() { {"authorization","Bearer " + AyehuHelper.JWTToken(apikey,password1,"HS256","JWT", 120)} };
            }
return _headers;
        }
        set {
            this._headers = value;
        }
    }
    
